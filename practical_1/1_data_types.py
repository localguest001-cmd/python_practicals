# Demonstration of data types

# integer
num1 = 10
num2 = 20
print("Integer addition:", num1 + num2)

# float
float1 = 3.14
float2 = 2.71
print("Float subtraction:", float1 - float2)

# complex
comp1 = 2 + 3j
comp2 = 4 + 5j
print("Complex multiplication:", comp1 * comp2)

# boolean
bool1 = True
bool2 = False
print("Boolean logical AND:", bool1 and bool2)

# string
name = "{{NAME}}"
age = 20
print("My name is " + name + " and I am " + str(age) + " years old.")

# input
user_input = input("Enter your name: ")
print("Hello,", user_input)
